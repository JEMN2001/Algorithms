string toMorse(string & str) {
	string out = "";
	char temp;
	for (size_t i = 0; i < str.size(); ++i) {
		temp = str.at(i);
		if ((temp >= 'A' && temp <= 'Z') || (temp >= 'a' && temp <= 'z') || (temp >= '0' && temp <= '9')) {
			out += chmo[str.at(i)]+" ";
		}
		else if (temp == ' ') {
			out += "  ";
		}
	}
	if (out.size() > 0) {
		out = out.substr(0, out.size()-1);
	}
	return out;
}
